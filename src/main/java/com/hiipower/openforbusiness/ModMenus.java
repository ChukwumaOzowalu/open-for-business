package com.hiipower.openforbusiness;

import com.hiipower.openforbusiness.menu.RegisterMenu;
import net.minecraft.world.inventory.MenuType;
import net.minecraftforge.common.extensions.IForgeMenuType;
import net.minecraftforge.eventbus.api.IEventBus;
import net.minecraftforge.registries.DeferredRegister;
import net.minecraftforge.registries.ForgeRegistries;
import net.minecraftforge.registries.RegistryObject;

public class ModMenus {
    public static final DeferredRegister<MenuType<?>> MENUS =
            DeferredRegister.create(ForgeRegistries.MENU_TYPES, OpenForBusiness.MODID);

    public static final RegistryObject<MenuType<RegisterMenu>> REGISTER_MENU =
            MENUS.register("register", () -> IForgeMenuType.create(RegisterMenu::new));

    public static void register(IEventBus bus) {
        MENUS.register(bus);
    }
}
